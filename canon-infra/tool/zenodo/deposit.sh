#   echo "[deposit.sh] PUBLISHING (IRREVERSIBLE)..."
#   PUBLISH_RESPONSE="$(curl -fsSL \
#     -H "Authorization: Bearer $ZENODO_TOKEN" \
#     -X POST \
#     "${ZENODO_API_HOST}/api/deposit/depositions/${DEP_ID}/actions/publish")"
#   DOI="$(echo "$PUBLISH_RESPONSE" | jq -r '.doi')"
#   echo "[deposit.sh] DOI minted: ${DOI}"
#   echo "$DOI" > "${ZENODO_DIR}/.doi"
# else
#   echo "[deposit.sh] step 5 skipped (no irreversibility flag)."
# fi

exit 0
