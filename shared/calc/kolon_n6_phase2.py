n, sigma, phi, tau, mu, sopfr, J2 = 6, 12, 2, 4, 1, 5, 24
R6 = 1

PASS = 0
FAIL = 0
TOTAL = 0

def check(domain, name, actual, expected, expr=""):
    global PASS, FAIL, TOTAL
    TOTAL += 1
    ok = abs(actual - expected) < 1e-6 if isinstance(actual, float) else actual == expected
    if ok:
        PASS += 1
    mark = "EXACT" if ok else "FAIL"
    if not ok:
        FAIL += 1
    expr_str = f" = {expr}" if expr else ""
    print(f"  [{mark}] {name}: {actual}{expr_str} (기대: {expected})")
    return ok

