PASS = 0
FAIL = 0
TOTAL = 0

def check(domain, name, actual, expected, expr=""):
    global PASS, FAIL, TOTAL
    TOTAL += 1
    ok = abs(actual - expected) < 1e-9 if isinstance(actual, float) else actual == expected
    mark = "EXACT" if ok else "FAIL"
    if ok:
        PASS += 1
    else:
        FAIL += 1
    expr_str = f" = {expr}" if expr else ""
    print(f"  [{mark}] {name}: {actual}{expr_str} (기대: {expected})")
    return ok


