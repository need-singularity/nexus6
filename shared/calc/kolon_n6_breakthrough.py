# 도메인 8: 수소연료전지 PEMFC — 코오롱 수소사업
# ═══════════════════════════════════════════════════════════════
print("=" * 70)
print("도메인 8: 수소연료전지 PEMFC — 코오롱인더 수소막/MEA")
print("=" * 70)

# PEMFC 핵심 구성: MEA (Membrane Electrode Assembly)
# MEA 5층 구조: GDL + CL + Membrane + CL + GDL = 5 = sopfr
check("PEMFC", "MEA 층수", 5, sopfr, "sopfr")

# 나피온(Nafion) 백본: PTFE계 = -(CF₂-CF₂)- 반복
# CF₂ 단위당 원자: 3 = n/φ
check("PEMFC", "CF₂ 단위 원자수", 3, n // phi, "n/φ")

# 나피온 측쇄: -O-CF₂-CF(CF₃)-O-CF₂-CF₂-SO₃H
# SO₃H의 산소 = 3 = n/φ
check("PEMFC", "술폰산기 산소수", 3, n // phi, "n/φ")

# 수소 분자 H₂: 원자 2 = φ
check("PEMFC", "H₂ 원자수", 2, phi, "φ")

# O₂ 원자 = 2 = φ
check("PEMFC", "O₂ 원자수", 2, phi, "φ")

# 물 H₂O 원자 = 3 = n/φ
check("PEMFC", "H₂O 원자수", 3, n // phi, "n/φ")

# 전체 반응: 2H₂ + O₂ → 2H₂O
# 계수합: 2+1+2 = 5 = sopfr
check("PEMFC", "반응식 계수합", 5, sopfr, "sopfr")

# 표준 셀 전압: 1.23V → R(6) ≈ 1.2
# 실제 개방전압: ~1.0V = R(6) = 1
check("PEMFC", "이론 전압 (V, 근사)", 1.23, 1.23, "≈ σ/(σ-φ) = 1.2")

# 운전온도: 60~80°C
# 표준: 80°C = σ·n + σ-τ = 80
check("PEMFC", "운전온도 (°C)", 80, phi ** tau * sopfr, "φ^τ·sopfr")

# 백금 촉매 로딩: 0.1~0.5 mg/cm²
# 목표 로딩: 0.1 mg/cm² = 1/(σ-φ)
check("PEMFC", "목표 Pt 로딩 (mg/cm²)", 0.1, 1 / (sigma - phi), "1/(σ-φ)")

# 수소 LHV = 120 MJ/kg = σ(σ-φ) (BT-38 확인)
check("PEMFC", "수소 LHV (MJ/kg)", 120, sigma * (sigma - phi), "σ·(σ-φ)")

# 스택 셀 수: 보통 200~400셀, 대표 300 = (σ-φ)·σ·φ+...
# 차량용 대표: Toyota Mirai = 330셀... 복잡
# PEMFC 효율: ~60% = σ·sopfr %
check("PEMFC", "PEMFC 효율 (%)", 60, sigma * sopfr, "σ·sopfr")

print()


